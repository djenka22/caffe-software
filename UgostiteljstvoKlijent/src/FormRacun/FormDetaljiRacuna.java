    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed

        int result = JOptionPane.showConfirmDialog(this, "Da li ste sigurni da zelite da "
                + "sacuvate izmene? Ova akcija se ne moze ponistiti!",
                "Konfirmacija", JOptionPane.YES_NO_OPTION);

        if (result == JOptionPane.NO_OPTION) {
            return;
        }

        if (result == JOptionPane.YES_OPTION) {

            double ukupanIznos = Double.parseDouble(txtUkupanIznos.getText());
            boolean obradjen = cbObradjen.isSelected();

            r.setUkupanIznos(ukupanIznos);
            r.setObradjen(obradjen);
            r.setStavkeRacuna(noveStavke);
            r.setDatumVreme(new Date());

            try {
                ClientController.getInstance().updateRacun(r);
                MainForm mf = (MainForm) getParent();
                mf.refreshTable();
                JOptionPane.showMessageDialog(this, "Uspesno sacuvane izmene.");
                this.dispose();
            } catch (Exception ex) {
                Logger.getLogger(FormDetaljiRacuna.class.getName()).log(Level.SEVERE, null, ex);
            }

        }

    }//GEN-LAST:event_btnIzmeniActionPerformed

    private void cmbProizvodItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbProizvodItemStateChanged

        if (cmbProizvod.getSelectedItem() != null && !txtKolicina.getText().isEmpty()) {
            Proizvod p = (Proizvod) cmbProizvod.getSelectedItem();
            int kolicina = Integer.parseInt(txtKolicina.getText());

            txtCena.setText(String.valueOf(p.getCena() * kolicina));
        }
    }//GEN-LAST:event_cmbProizvodItemStateChanged

    private void txtKolicinaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtKolicinaKeyReleased

        if (evt.getKeyChar() >= '0' && evt.getKeyChar() <= '9') {

            Proizvod p = (Proizvod) cmbProizvod.getSelectedItem();
            int kolicina = Integer.parseInt(txtKolicina.getText());

            txtCena.setText(String.valueOf(p.getCena() * kolicina));

        } else {

            txtKolicina.setText("");
            txtCena.setText("0.00");

        }
    }//GEN-LAST:event_txtKolicinaKeyReleased

    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed

        if (txtKolicina.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Morate uneti kolicinu!");
            return;
        }

        Proizvod p = (Proizvod) cmbProizvod.getSelectedItem();
        int kolicina = Integer.parseInt(txtKolicina.getText());
        double cena = Double.parseDouble(txtCena.getText());

        StavkaRacuna sr = new StavkaRacuna(r, -1, kolicina, cena, p);

        TableModelStavke tm = (TableModelStavke) tblStavke.getModel();

        int result = JOptionPane.showConfirmDialog(this, "Da li ste sigurni da zelite da "
                + "dodate " + p.getNazivProizvoda() + " x" + kolicina + " ? Ova akcija se ne moze"
                + " ponistiti nakon cuvanja izmena!",
                "Konfirmacija", JOptionPane.YES_NO_OPTION);

        if (result == JOptionPane.NO_OPTION) {
            return;
        }

        if (result == JOptionPane.YES_OPTION) {
            int rb = tm.dodajStavku(sr);

            ukupanIznos = tm.getUkupnaCena();

            txtUkupanIznos.setText(String.valueOf(ukupanIznos));

            sr.setRbStavke(rb);

            noveStavke.add(sr);
        }

    }//GEN-LAST:event_btnDodajActionPerformed


