    private void srediFormu() {
        lblSto.setText(r.getSto().toString());
        txtUkupanIznos.setText(String.valueOf(r.getUkupanIznos()));
        popuniProizvode();
        ukupanIznos = r.getUkupanIznos();
        tblStavke.setModel(new TableModelStavke(r));
        TableModelStavke tm = (TableModelStavke) tblStavke.getModel();
        postojeceStavke = tm.getLista();
        txtUkupanIznos.setEditable(false);
        txtCena.setEditable(false);

        if (r.isObradjen()) {
            cbObradjen.setSelected(true);
            cbObradjen.setEnabled(false);
            cmbProizvod.setEnabled(false);
            txtKolicina.setEnabled(false);
            btnDodaj.setEnabled(false);
            btnIzmeni.setEnabled(false);
        }

    }

    private void popuniProizvode() {
        try {
            ArrayList<Proizvod> proizvodi = ClientController.getInstance().getAllProizvod();

            cmbProizvod.removeAllItems();

            for (Proizvod proizvod : proizvodi) {
                cmbProizvod.addItem(proizvod);

            }

        } catch (Exception ex) {
            Logger.getLogger(MainForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

